function fonteh1(e){
    var elemento = $(".acessibilidadeh1");
    var fonte = elemento.css('font-size');
    if (e == 'a') {
        elemento.css("fontSize", parseInt(fonte) + 2);
    } else if('d'){
        elemento.css("fontSize", parseInt(fonte) - 2);
    }
}
